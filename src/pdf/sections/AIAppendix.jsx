/**
 * AIAppendix — chapter 13. ONLY rendered when narrativeMode is true.
 *
 * Four AI-only sub-sections, in order:
 *   • DM Compass       — 3 ready-to-run hooks, 2 red-flag warnings, 1 twist
 *   • Identity Markers — 4–6 concrete sensory / physical details
 *   • Friction Points  — 3–5 named small-scale interpersonal grievances
 *   • Connections Map  — 4–8 NPC ↔ faction / institution edges
 *
 * Visually distinct from the rest of the dossier: purple accent rule, AI
 * tint background tones, and an "AI-AUTHORED" eyebrow on every sub-block so
 * the GM remembers this content was generated, not derived from raw rules.
 */
import React from 'react';
import { View, Text } from '@react-pdf/renderer';
import { PageChrome } from '../primitives/PageChrome.jsx';
import { Section } from '../primitives/Section.jsx';
import { Heading } from '../primitives/Heading.jsx';
import { Pill } from '../primitives/Pill.jsx';
import { Callout } from '../primitives/Callout.jsx';
import { type, palette, space } from '../theme.js';

export function AIAppendix({ settlement, narrativeMode, vm }) {
  // Defence-in-depth: only render when narrative content actually exists.
  if (!narrativeMode || !vm.aiAppendix) return null;
  const a = vm.aiAppendix;

  const hasDmCompass = !!(a.dmCompass && (
    (a.dmCompass.hooks || []).length ||
    (a.dmCompass.redFlags || []).length ||
    a.dmCompass.twist
  ));
  const hasIdentity = (a.identityMarkers || []).length > 0;
  const hasFriction = (a.frictionPoints || []).length > 0;
  const hasConnections = (a.connectionsMap || []).length > 0;

  if (!hasDmCompass && !hasIdentity && !hasFriction && !hasConnections) {
    return null;
  }

  return (
    <PageChrome settlement={settlement} narrativeMode={narrativeMode}>
      <Section eyebrow="13" title="AI Appendix" accent={palette.ai}>
        <Text style={{ ...type.italic, color: palette.muted, fontSize: 10, marginBottom: space.md }}>
          Generated by the AI narrative pass. Use these as session prep prompts; rewrite
          freely to fit your table.
        </Text>

        {/* ── DM COMPASS ──────────────────────────────────────────── */}
        {hasDmCompass && (
          <View style={{ marginBottom: space.lg }}>
            <Heading level={3} color={palette.ai}>DM Compass</Heading>

            {(a.dmCompass.hooks || []).length > 0 && (
              <View style={{ marginBottom: space.sm }}>
                <Text style={{ ...type.label, color: palette.ai, fontSize: 7.5, marginBottom: 6 }}>
                  RUN-READY HOOKS
                </Text>
                {a.dmCompass.hooks.map((h, i) => (
                  <View
                    key={`h-${i}`}
                    style={{
                      flexDirection: 'row',
                      alignItems: 'flex-start',
                      paddingVertical: 6,
                      borderBottom:
                        i < a.dmCompass.hooks.length - 1
                          ? `0.3pt solid ${palette.border}`
                          : undefined,
                    }}
                    wrap={false}
                  >
                    <Text
                      style={{
                        ...type.label_em,
                        color: palette.ai,
                        width: 24,
                        fontSize: 9,
                      }}
                    >
                      {String(i + 1).padStart(2, '0')}
                    </Text>
                    <Text style={{ ...type.body, flex: 1 }}>{textOf(h)}</Text>
                  </View>
                ))}
              </View>
            )}

            {(a.dmCompass.redFlags || []).length > 0 && (
              <View style={{ marginBottom: space.sm }}>
                <Text style={{ ...type.label, color: palette.bad, fontSize: 7.5, marginBottom: 6 }}>
                  RED FLAGS — WATCH FOR
                </Text>
                {a.dmCompass.redFlags.map((f, i) => (
                  <View key={`rf-${i}`} style={{ flexDirection: 'row', marginBottom: 4 }} wrap={false}>
                    <Text style={{ ...type.body_em, color: palette.bad, marginRight: 6 }}>!</Text>
                    <Text style={{ ...type.body, flex: 1 }}>{textOf(f)}</Text>
                  </View>
                ))}
              </View>
            )}

            {a.dmCompass.twist && (
              <Callout tone="ai" kicker="WHEN A SESSION DRAGS — THIS TWIST">
                <Text style={{ ...type.italic, color: palette.ink, fontSize: 10.5, lineHeight: 1.45 }}>
                  {textOf(a.dmCompass.twist)}
                </Text>
              </Callout>
            )}
          </View>
        )}

        {/* ── IDENTITY MARKERS ────────────────────────────────────── */}
        {hasIdentity && (
          <View style={{ marginBottom: space.lg }}>
            <Heading level={3} color={palette.ai}>Identity Markers</Heading>
            <Text style={{ ...type.caption, color: palette.muted, marginBottom: 6 }}>
              Concrete sensory or physical details for first-impression descriptions.
            </Text>
            <View style={{ flexDirection: 'row', flexWrap: 'wrap', gap: 6 }}>
              {a.identityMarkers.map((m, i) => (
                <View
                  key={`im-${i}`}
                  style={{
                    width: '48%',
                    padding: 8,
                    backgroundColor: palette.aiTint,
                    borderLeft: `2pt solid ${palette.ai}`,
                    borderRadius: 2,
                    marginBottom: 6,
                  }}
                  wrap={false}
                >
                  {m.label && (
                    <Text style={{ ...type.label, color: palette.ai, fontSize: 7.5, marginBottom: 2 }}>
                      {m.label.toUpperCase()}
                    </Text>
                  )}
                  <Text style={{ ...type.body, color: palette.ink }}>{textOf(m)}</Text>
                </View>
              ))}
            </View>
          </View>
        )}

        {/* ── FRICTION POINTS ─────────────────────────────────────── */}
        {hasFriction && (
          <View style={{ marginBottom: space.lg }}>
            <Heading level={3} color={palette.ai}>Friction Points</Heading>
            <Text style={{ ...type.caption, color: palette.muted, marginBottom: 6 }}>
              Small-scale interpersonal grievances with named parties — perfect for
              opening or closing scenes.
            </Text>
            {a.frictionPoints.map((fp, i) => (
              <View
                key={`fp-${i}`}
                style={{
                  flexDirection: 'row',
                  alignItems: 'flex-start',
                  paddingVertical: 6,
                  borderBottom:
                    i < a.frictionPoints.length - 1
                      ? `0.3pt solid ${palette.border}`
                      : undefined,
                }}
                wrap={false}
              >
                <Text
                  style={{
                    ...type.label_em,
                    color: palette.ai,
                    width: 24,
                    fontSize: 9,
                  }}
                >
                  {String(i + 1).padStart(2, '0')}
                </Text>
                <View style={{ flex: 1 }}>
                  {fp.parties && (
                    <Text style={{ ...type.label, color: palette.muted, fontSize: 7.5, marginBottom: 1 }}>
                      {(Array.isArray(fp.parties) ? fp.parties.join(' ↔ ') : String(fp.parties)).toUpperCase()}
                    </Text>
                  )}
                  <Text style={type.body}>{textOf(fp)}</Text>
                </View>
              </View>
            ))}
          </View>
        )}

        {/* ── CONNECTIONS MAP ─────────────────────────────────────── */}
        {hasConnections && (
          <View>
            <Heading level={3} color={palette.ai}>Connections Map</Heading>
            <Text style={{ ...type.caption, color: palette.muted, marginBottom: 6 }}>
              Who knows whom, who owes whom, who answers to whom.
            </Text>
            {a.connectionsMap.map((c, i) => (
              <View
                key={`cm-${i}`}
                style={{
                  flexDirection: 'row',
                  alignItems: 'center',
                  paddingVertical: 5,
                  borderBottom:
                    i < a.connectionsMap.length - 1 ? `0.3pt solid ${palette.border}` : undefined,
                }}
                wrap={false}
              >
                <Text style={{ ...type.body_em, color: palette.ink, width: 130 }}>
                  {endpointOf(c.from || c.source || c.a)}
                </Text>
                <View style={{ width: 90 }}>
                  <Pill tone="ai">{relationshipOf(c)}</Pill>
                </View>
                <Text style={{ ...type.body_em, color: palette.ink, width: 130 }}>
                  {endpointOf(c.to || c.target || c.b)}
                </Text>
                {c.note && (
                  <Text style={{ ...type.caption, color: palette.muted, flex: 1, marginLeft: 8 }}>
                    {c.note}
                  </Text>
                )}
              </View>
            ))}
          </View>
        )}
      </Section>
    </PageChrome>
  );
}

function textOf(item) {
  if (!item) return '';
  if (typeof item === 'string') return item;
  return (
    item.text || item.description || item.summary || item.label || item.title || item.detail || ''
  );
}

function endpointOf(e) {
  if (!e) return '—';
  if (typeof e === 'string') return e;
  return e.name || e.label || e.title || '—';
}

function relationshipOf(c) {
  if (!c) return 'LINK';
  return (c.relationship || c.type || c.kind || 'LINK').toUpperCase();
}

export default AIAppendix;
