/**
 * domain/userEdits.js — per-field user authorship.
 *
 * Tier 5.4 of the roadmap. Premium users can hand-write descriptive
 * prose for any field in the dossier (NPC secrets, plot hooks,
 * institution descriptions, history beats, etc.). This module is the
 * pure-domain layer underneath the UI:
 *
 *   applyUserEdit(entity, path, newValue)  -> mutates entity, records original
 *   revertUserEdit(entity, path)           -> restores original, clears the record
 *   getEditedPaths(entity)                  -> list of edited paths
 *   isEdited(entity, path)                  -> boolean
 *   getOriginalValue(entity, path)          -> the pre-edit string, or null
 *   isSettlementEdited(settlement)          -> true if ANY field anywhere is edited
 *   countSettlementEdits(settlement)        -> total count across the whole tree
 *   walkUserEdits(settlement)               -> iterate every edit in the tree
 *
 * The edit record lives on the entity itself as a single `_userEdits`
 * object:
 *
 *   entity._userEdits = {
 *     'secret.what': {
 *       value:         'Hand-written by the DM.',
 *       originalValue: 'Generated by the engine.',
 *       editedAt:      '2026-05-20T09:00:00.000Z',
 *     },
 *   };
 *
 * AND `_authored: true` is set on the entity so the existing
 * domain/canonStatus.js tagger automatically promotes it to
 * `source: 'user'`, `canonStatus: 'canon'`, `locked: true`. Downstream
 * consumers (AI grounding, overlay verifier, engine regenerate)
 * already respect that flag — no new wiring needed for canon
 * preservation.
 *
 * Pure, synchronous, no I/O. Callers either pass a draft (Immer) or a
 * mutable clone they own.
 */

// ── Editable-field registry ────────────────────────────────────────────
//
// Tightly scoped to PROSE ONLY. Structural fields (population, tier,
// faction power, severity bands) are not editable here — those would
// invalidate the simulation math and need their own opt-in surface
// with confirmation.
//
// The keys are entity types; the values are arrays of dotted paths
// the UI may expose for editing. Use `isEditablePath(type, path)` to
// gate user input.

export const EDITABLE_FIELDS = Object.freeze({
  npc: Object.freeze([
    'goal.short',
    'secret.what',
    'personality',
    'role',
  ]),
  faction: Object.freeze([
    'desc',
  ]),
  institution: Object.freeze([
    'desc',
  ]),
  hook: Object.freeze([
    'description',
    'stakes',
  ]),
  historicalEvent: Object.freeze([
    'description',
    'summary',
  ]),
  currentTension: Object.freeze([
    'description',
  ]),
  // Settlement-root prose fields (no enclosing entity).
  settlement: Object.freeze([
    'arrivalScene',
    'pressureSentence',
    'settlementReason',
    'prominentRelationship.phrasing',
    'history.historicalCharacter',
    'history.founding.reason',
    'history.founding.initialChallenge',
    'history.founding.overcoming',
    'history.founding.stressNote',
    'history.founding.foundedBy',
    'economicViability.summary',
    'economicState.safetyProfile.safetyDesc',
    'economicState.safetyProfile.guardEffectivenessDesc',
    'economicState.safetyProfile.economicDragDesc',
  ]),
});

/**
 * Closed vocabulary of editable entity types.
 */
export const EDITABLE_ENTITY_TYPES = Object.freeze(Object.keys(EDITABLE_FIELDS));

/**
 * Is the given (type, path) pair user-editable? Type is one of
 * EDITABLE_ENTITY_TYPES. Returns false for unknown types / paths so
 * the UI can use this as a strict gate.
 */
export function isEditablePath(type, path) {
  const allowed = EDITABLE_FIELDS[type];
  if (!allowed) return false;
  return allowed.includes(path);
}

// ── Path helpers ───────────────────────────────────────────────────────

function getAtPath(obj, path) {
  if (obj == null) return undefined;
  const keys = path.split('.');
  let ref = obj;
  for (const k of keys) {
    if (ref == null || typeof ref !== 'object') return undefined;
    ref = ref[k];
  }
  return ref;
}

function setAtPath(obj, path, value) {
  if (obj == null) return obj;
  const keys = path.split('.');
  let ref = obj;
  for (let i = 0; i < keys.length - 1; i++) {
    const k = keys[i];
    if (ref[k] == null || typeof ref[k] !== 'object') ref[k] = {};
    ref = ref[k];
  }
  ref[keys[keys.length - 1]] = value;
  return obj;
}

// ── Public API: per-entity ─────────────────────────────────────────────

/**
 * Apply a user edit to `entity` at `path`. Mutates the entity.
 *
 * Records the pre-edit value on the FIRST edit to that path so
 * `revertUserEdit` can restore it. Subsequent edits on the same path
 * update the current value and the timestamp but leave the original
 * intact — Revert always restores the truly-original value.
 *
 * Sets `entity._authored = true` so canonStatus picks it up as
 * `source: 'user'` automatically.
 *
 * @param {Object} entity         The entity to mutate (caller-owned).
 * @param {string} path           Dotted path to the field.
 * @param {string} newValue       The user-authored value.
 * @param {Object} [options]
 * @param {string} [options.editedAt]  ISO timestamp (defaults to now).
 * @returns {Object} The same entity (for chaining).
 */
export function applyUserEdit(entity, path, newValue, options = {}) {
  if (!entity || typeof entity !== 'object' || typeof path !== 'string' || !path) {
    return entity;
  }
  const editedAt = options.editedAt || new Date().toISOString();

  if (!entity._userEdits || typeof entity._userEdits !== 'object') {
    entity._userEdits = {};
  }

  if (!(path in entity._userEdits)) {
    // First edit: capture the original.
    entity._userEdits[path] = {
      value: newValue,
      originalValue: getAtPath(entity, path),
      editedAt,
    };
  } else {
    // Subsequent edit: update value + timestamp, KEEP originalValue.
    entity._userEdits[path].value = newValue;
    entity._userEdits[path].editedAt = editedAt;
  }

  setAtPath(entity, path, newValue);
  entity._authored = true;
  return entity;
}

/**
 * Revert a user edit at `path` to its original value. Removes the
 * `_userEdits` record. If no edits remain on the entity, clears
 * `_userEdits` and `_authored` so canonStatus reverts to inferring
 * `source: 'generated'`.
 *
 * No-op if the path isn't edited.
 *
 * @param {Object} entity
 * @param {string} path
 * @returns {Object} The same entity (for chaining).
 */
export function revertUserEdit(entity, path) {
  if (!entity || typeof entity !== 'object') return entity;
  if (!entity._userEdits || !(path in entity._userEdits)) return entity;

  const record = entity._userEdits[path];
  setAtPath(entity, path, record.originalValue);
  delete entity._userEdits[path];

  if (Object.keys(entity._userEdits).length === 0) {
    delete entity._userEdits;
    delete entity._authored;
  }
  return entity;
}

/**
 * Revert EVERY user edit on `entity`. Convenience for the
 * "Revert all" affordance.
 */
export function revertAllEdits(entity) {
  if (!entity || typeof entity !== 'object' || !entity._userEdits) return entity;
  for (const path of Object.keys(entity._userEdits)) {
    revertUserEdit(entity, path);
  }
  return entity;
}

/**
 * List of dotted paths that have been user-edited on this entity.
 * Returns [] for entities with no edits.
 */
export function getEditedPaths(entity) {
  if (!entity || !entity._userEdits) return [];
  return Object.keys(entity._userEdits);
}

/**
 * Is the given path user-edited on this entity?
 */
export function isEdited(entity, path) {
  return !!(entity && entity._userEdits && Object.prototype.hasOwnProperty.call(entity._userEdits, path));
}

/**
 * Does the entity have any user edits at all?
 */
export function hasAnyEdit(entity) {
  return getEditedPaths(entity).length > 0;
}

/**
 * The pre-edit value at `path`. Returns null if the path is not
 * edited. Use this for "Revert to generated" tooltips.
 */
export function getOriginalValue(entity, path) {
  if (!isEdited(entity, path)) return null;
  return entity._userEdits[path].originalValue ?? null;
}

/**
 * The current effective value at `path` (edited or otherwise). Always
 * returns the live value — the underlying field stays in sync with
 * the edit record, so this is equivalent to a path read. Provided so
 * callers don't need to know whether a field is edited.
 */
export function getEffectiveValue(entity, path) {
  return getAtPath(entity, path);
}

/**
 * The full edit record at `path` ({ value, originalValue, editedAt })
 * or null if not edited. Useful for UI surfaces that want to show the
 * timestamp.
 */
export function getEditRecord(entity, path) {
  if (!isEdited(entity, path)) return null;
  return entity._userEdits[path];
}

// ── Public API: settlement-level walking ───────────────────────────────

/**
 * The list of entity arrays inside a settlement we recurse into when
 * counting edits. Centralized so adding a new editable entity type
 * touches only this list.
 */
const ENTITY_ARRAY_PATHS = Object.freeze([
  ['npcs'],
  ['institutions'],
  ['supplyChains'],
  ['activeConditions'],
  ['hooks'],
  ['plotHooks'],
  ['powerStructure', 'factions'],
  ['powerStructure', 'conflicts'],
  ['history', 'historicalEvents'],
  ['history', 'currentTensions'],
]);

function readNestedArray(settlement, segments) {
  let ref = settlement;
  for (const seg of segments) {
    if (ref == null || typeof ref !== 'object') return null;
    ref = ref[seg];
  }
  return Array.isArray(ref) ? ref : null;
}

/**
 * Iterate every (entity, editedPath, record) tuple in the settlement
 * — root edits as well as per-entity edits. Yields:
 *
 *   { kind: 'settlement', path: 'arrivalScene', record }
 *   { kind: 'npc', entityIndex: 3, entity, path: 'secret.what', record }
 *   { kind: 'faction', entityIndex: 1, entity, path: 'desc', record }
 *
 * `kind` is the singular entity type ('npc' / 'faction' / 'institution'
 * / 'historicalEvent' / 'currentTension' / 'hook' / 'plotHook' /
 * 'condition' / 'supplyChain' / 'settlement').
 */
export function walkUserEdits(settlement) {
  const out = [];
  if (!settlement || typeof settlement !== 'object') return out;

  // Settlement root edits first.
  for (const path of getEditedPaths(settlement)) {
    out.push({
      kind: 'settlement',
      entityIndex: -1,
      entity: settlement,
      path,
      record: settlement._userEdits[path],
    });
  }

  const KIND_BY_FIELD = {
    npcs: 'npc',
    institutions: 'institution',
    supplyChains: 'supplyChain',
    activeConditions: 'condition',
    hooks: 'hook',
    plotHooks: 'plotHook',
    factions: 'faction',
    conflicts: 'conflict',
    historicalEvents: 'historicalEvent',
    currentTensions: 'currentTension',
  };

  for (const segments of ENTITY_ARRAY_PATHS) {
    const arr = readNestedArray(settlement, segments);
    if (!arr) continue;
    const leaf = segments[segments.length - 1];
    const kind = KIND_BY_FIELD[leaf] || leaf;
    arr.forEach((entity, idx) => {
      for (const path of getEditedPaths(entity)) {
        out.push({
          kind,
          entityIndex: idx,
          entity,
          path,
          record: entity._userEdits[path],
        });
      }
    });
  }
  return out;
}

/**
 * Total count of user edits anywhere in the settlement tree.
 */
export function countSettlementEdits(settlement) {
  return walkUserEdits(settlement).length;
}

/**
 * True if any field anywhere in the settlement is user-edited. Powers
 * the "Edited" badge on the dossier header.
 */
export function isSettlementEdited(settlement) {
  return countSettlementEdits(settlement) > 0;
}

/**
 * A compact `[ "kind: label > path" ]` summary suitable for tooltips
 * and audit logs. Entity labels default to the entity's `name` /
 * `faction` / index when no name is present.
 */
export function summarizeUserEdits(settlement) {
  const tuples = walkUserEdits(settlement);
  return tuples.map(({ kind, entity, entityIndex, path }) => {
    if (kind === 'settlement') return `settlement > ${path}`;
    const label = entity?.name || entity?.faction || `#${entityIndex}`;
    return `${kind}: ${label} > ${path}`;
  });
}
