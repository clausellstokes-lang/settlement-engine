#!/usr/bin/env node
/**
 * scripts/build-edge-shared.mjs - Tier 6.8 edge-function bundle builder.
 *
 * Bundles src/domain/aiGrounding.js (plus every transitive
 * domain/data dependency it reaches) into a single ESM file the
 * Supabase Edge Function (Deno) imports. The bundle ships in tree at
 * `supabase/functions/_shared/aiGroundingBundle.js` so `supabase
 * functions deploy` doesn't need a build step - it just packages the
 * existing tree.
 *
 * Freshness contract: re-run after editing any module under
 * `src/domain/` that aiGrounding transitively imports. The
 * `aiGrounding-bundle.freshness.test.js` test fails on stale bundles.
 *
 * Run with:
 *   node scripts/build-edge-shared.mjs
 *
 * Or via npm:
 *   npm run build:edge-shared
 */

import { build } from 'esbuild';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';
import { writeFileSync, readFileSync, existsSync, mkdirSync } from 'node:fs';
import { createHash } from 'node:crypto';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT     = join(__dirname, '..');
const ENTRY    = join(ROOT, 'src', 'domain', 'aiGrounding.js');
const OUT_DIR  = join(ROOT, 'supabase', 'functions', '_shared');
const OUT_FILE = join(OUT_DIR, 'aiGroundingBundle.js');
const META_FILE = join(OUT_DIR, 'aiGroundingBundle.meta.json');

const BANNER = `/**
 * Auto-generated by scripts/build-edge-shared.mjs - DO NOT EDIT BY HAND.
 *
 * This file is the Deno-importable bundle of src/domain/aiGrounding.js
 * plus every transitive domain dep it reaches. Regenerate after
 * editing anything under src/domain/ via:
 *
 *   npm run build:edge-shared
 *
 * Freshness is enforced by tests/edgeFunctions/aiGrounding-bundle.freshness.test.js
 * (Tier 6.8). The freshness test compares the source-tree hash to the
 * hash embedded in this file's header; CI fails on stale bundles.
 */
`;

if (!existsSync(OUT_DIR)) mkdirSync(OUT_DIR, { recursive: true });

const result = await build({
  entryPoints: [ENTRY],
  outfile: OUT_FILE,
  bundle: true,
  format: 'esm',
  target: 'es2022',          // Deno supports modern ES features
  platform: 'neutral',        // No Node / browser globals
  charset: 'utf8',
  // Don't minify - readable bundles make debugging easier and the
  // edge-function payload size is already dominated by data tables.
  minify: false,
  // Don't inline source maps in the committed bundle.
  sourcemap: false,
  // Surface unresolved imports clearly so the bundle can never load
  // a stub that throws at runtime.
  logLevel: 'info',
  // Each external value would require a runtime import. We bundle
  // EVERYTHING the entry transitively touches so the edge function
  // can load just the one file.
  external: [],
  banner: { js: BANNER },
  metafile: true,
});

// Hash the bundled inputs so the freshness test can detect drift
// without re-bundling. metafile.inputs is { path: { bytes, imports } }.
const inputPaths = Object.keys(result.metafile.inputs).sort();
const inputContents = inputPaths.map(p => `${p}:${readFileSync(join(ROOT, p), 'utf8')}`).join('\n');
const sourceHash = createHash('sha256').update(inputContents).digest('hex').slice(0, 16);

// Embed the hash + input list into a sidecar JSON so the freshness
// test doesn't have to re-bundle on every run.
const meta = {
  generatedAt: new Date().toISOString(),
  entry: 'src/domain/aiGrounding.js',
  sourceHash,
  inputs: inputPaths,
};
writeFileSync(META_FILE, JSON.stringify(meta, null, 2) + '\n');

// Also embed the hash into the bundle's banner for in-band checking.
const bundleSrc = readFileSync(OUT_FILE, 'utf8');
const tagged = bundleSrc.replace(
  '// Auto-generated by scripts/build-edge-shared.mjs - DO NOT EDIT BY HAND.',
  `// Auto-generated by scripts/build-edge-shared.mjs - DO NOT EDIT BY HAND.\n// Source hash: ${sourceHash}`,
);
// banner is a multi-line comment so the literal lookup string differs slightly
const finalSrc = tagged.includes('Source hash:')
  ? tagged
  : bundleSrc.replace('DO NOT EDIT BY HAND.', `DO NOT EDIT BY HAND.\n * Source hash: ${sourceHash}`);
writeFileSync(OUT_FILE, finalSrc);

const bundleBytes = Buffer.byteLength(finalSrc, 'utf8');
console.log(`✓ Bundled aiGrounding for Deno edge function`);
console.log(`  output:  ${OUT_FILE}`);
console.log(`  inputs:  ${inputPaths.length}`);
console.log(`  size:    ${(bundleBytes / 1024).toFixed(1)} KB`);
console.log(`  hash:    ${sourceHash}`);
