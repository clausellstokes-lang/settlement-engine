#!/usr/bin/env node
/**
 * scripts/build-edge-shared.mjs — edge-function shared-bundle builder.
 *
 * Bundles selected pure src modules (plus their transitive deps) into single
 * ESM files the Supabase Edge Functions (Deno) import. Bundles ship in tree
 * under supabase/functions/_shared/ so `supabase functions deploy` needs no
 * build step — it just packages the existing tree.
 *
 * Entries (add one line to ENTRIES to bundle another module):
 *   - aiGrounding      (src/domain/aiGrounding.js)      → aiGroundingBundle.js
 *   - analyticsEvents  (src/lib/analyticsEvents.js)     → analyticsEventsBundle.js
 *
 * Freshness contract: re-run after editing any module a bundle transitively
 * imports. The per-bundle *.freshness.test.js tests fail on stale bundles.
 *
 *   node scripts/build-edge-shared.mjs      (or: npm run build:edge-shared)
 */

import { build } from 'esbuild';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';
import { writeFileSync, readFileSync, existsSync, mkdirSync } from 'node:fs';
import { createHash } from 'node:crypto';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT    = join(__dirname, '..');
const OUT_DIR = join(ROOT, 'supabase', 'functions', '_shared');

const ENTRIES = [
  { label: 'aiGrounding',     entry: 'src/domain/aiGrounding.js',  out: 'aiGroundingBundle.js',     meta: 'aiGroundingBundle.meta.json' },
  { label: 'analyticsEvents', entry: 'src/lib/analyticsEvents.js', out: 'analyticsEventsBundle.js', meta: 'analyticsEventsBundle.meta.json' },
];

function banner(entryRel) {
  return `/**
 * Auto-generated by scripts/build-edge-shared.mjs — DO NOT EDIT BY HAND.
 *
 * Deno-importable bundle of ${entryRel} plus every transitive dep it reaches.
 * Regenerate after editing any bundled module via:
 *
 *   npm run build:edge-shared
 *
 * Freshness is enforced by a *.freshness.test.js test that compares the
 * source-tree hash to the hash embedded in this file's header; CI fails stale.
 */
`;
}

if (!existsSync(OUT_DIR)) mkdirSync(OUT_DIR, { recursive: true });

async function buildOne({ label, entry, out, meta }) {
  const OUT_FILE = join(OUT_DIR, out);
  const META_FILE = join(OUT_DIR, meta);

  const result = await build({
    entryPoints: [join(ROOT, entry)],
    outfile: OUT_FILE,
    bundle: true,
    format: 'esm',
    target: 'es2022',
    platform: 'neutral',
    charset: 'utf8',
    minify: false,
    sourcemap: false,
    logLevel: 'info',
    external: [],
    banner: { js: banner(entry) },
    metafile: true,
  });

  const inputPaths = Object.keys(result.metafile.inputs).sort();
  const inputContents = inputPaths.map(p => `${p}:${readFileSync(join(ROOT, p), 'utf8')}`).join('\n');
  const sourceHash = createHash('sha256').update(inputContents).digest('hex').slice(0, 16);

  writeFileSync(META_FILE, JSON.stringify({
    generatedAt: new Date().toISOString(),
    entry,
    sourceHash,
    inputs: inputPaths,
  }, null, 2) + '\n');

  // Embed the hash into the banner for in-band checking (idempotent).
  const bundleSrc = readFileSync(OUT_FILE, 'utf8');
  const finalSrc = bundleSrc.includes('Source hash:')
    ? bundleSrc
    : bundleSrc.replace('DO NOT EDIT BY HAND.', `DO NOT EDIT BY HAND.\n * Source hash: ${sourceHash}`);
  writeFileSync(OUT_FILE, finalSrc);

  const kb = (Buffer.byteLength(finalSrc, 'utf8') / 1024).toFixed(1);
  console.log(`✓ ${label}: ${out}  (inputs: ${inputPaths.length}, ${kb} KB, hash ${sourceHash})`);
}

for (const e of ENTRIES) {
   
  await buildOne(e);
}
