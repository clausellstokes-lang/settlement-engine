/**
 * ESLint v9 flat config — focused, pragmatic ruleset.
 *
 * Philosophy: rules earn their place by catching the bugs we've
 * actually shipped. We don't enforce stylistic preferences (prettier's
 * job, and we don't run prettier here) — we catch correctness issues:
 *   - undeclared variables
 *   - unused variables (warns, doesn't fail, because the codebase has
 *     a lot of legitimate underscore-prefixed unused parameters)
 *   - misuse of React hooks (rules-of-hooks + exhaustive-deps)
 *   - duplicate object keys (the same class of bug that bit us in
 *     src/data once; this catches it at edit time, not at build)
 *   - missing `case` breaks in switches
 *
 * Scope: every JS/JSX file under src/ except the FMG fork in
 * public/map/main.js (legacy, untouchable) and node_modules.
 *
 * Severity bias: real correctness issues are errors; everything else
 * is a warning so the linter doesn't block work but surfaces issues.
 */

import js from '@eslint/js';
import reactHooks from 'eslint-plugin-react-hooks';
import jsxA11y from 'eslint-plugin-jsx-a11y';
import globals from 'globals';
import visualBudget from './scripts/eslint-plugin-visual-budget.js';
import analytics from './scripts/eslint-plugin-analytics.js';
import jsxHygiene from './scripts/eslint-plugin-jsx-hygiene.js';

// eslint-plugin-react doesn't yet support ESLint 10's flat-config
// resolver (throws on contextOrFilename.getFilename). We drop it and
// rely on react-hooks for the high-value rules (rules-of-hooks,
// exhaustive-deps). Once the plugin catches up, add it back here.

export default [
  {
    ignores: [
      'node_modules/**',
      'dist/**',
      'public/**',
      '.vercel/**',
      'supabase/.temp/**',
      // Generated coverage report (istanbul). Re-generated by
      // `npm run test:coverage`; linting it adds noise without value.
      'coverage/**',
    ],
  },

  // Base rules — recommended JS rules plus our additions.
  {
    files: ['src/**/*.{js,jsx}', 'tests/**/*.{js,jsx}', 'scripts/**/*.{js,mjs}'],
    languageOptions: {
      ecmaVersion: 'latest',
      sourceType: 'module',
      globals: { ...globals.browser, ...globals.node, ...globals.es2024 },
      parserOptions: { ecmaFeatures: { jsx: true } },
    },
    plugins: {
      'react-hooks': reactHooks,
      'visual-budget': visualBudget,
      'analytics': analytics,
      'jsx-hygiene': jsxHygiene,
    },
    rules: {
      ...js.configs.recommended.rules,
      ...reactHooks.configs.recommended.rules,

      // Correctness — error severity
      'no-dupe-keys': 'error',
      'no-dupe-args': 'error',
      'no-duplicate-case': 'error',
      // Duplicate JSX props (esbuild silently keeps the last) — eslint-plugin-
      // react's jsx-no-duplicate-props isn't loadable on ESLint 10, so this is
      // the local equivalent. Error: a dropped prop is a real bug.
      'jsx-hygiene/no-duplicate-jsx-props': 'error',
      'no-fallthrough': 'error',
      'no-self-assign': 'error',
      'no-unreachable': 'error',
      'no-constant-condition': ['error', { checkLoops: false }],

      // Hooks — error severity (the exhaustive-deps warnings are the
      // ones that catch real bugs, like the geometryVersion fix earlier)
      'react-hooks/rules-of-hooks': 'error',
      'react-hooks/exhaustive-deps': 'warn',

      // React 19 strict-mode rules — forward-looking checks that flag
      // patterns the React Compiler doesn't like (refs accessed during
      // render, impure functions during render, components defined
      // inside render, setState in effects, etc.). These don't cause
      // shipped bugs today but break under React Compiler. Demoted to
      // warnings so we see drift without blocking CI; promote to
      // errors when adopting React Compiler.
      'react-hooks/static-components':    'warn',
      'react-hooks/refs':                 'warn',
      'react-hooks/purity':               'warn',
      'react-hooks/set-state-in-effect':  'warn',
      'react-hooks/immutability':         'warn',

      // Stylistic / pragmatic — warn so we see drift without blocking
      // caughtErrorsIgnorePattern accepts the JS-idiomatic discard names
      // (`_`, `__`) AND the named-but-unused conventions (`e`, `err`,
      // `error`, optionally `_`-prefixed). Both are common in this
      // codebase — older sites used `catch (_)`, newer ones use
      // `catch (_e)`. Keeping both legal avoids a churn-only rename.
      'no-unused-vars': ['warn', { argsIgnorePattern: '^_', varsIgnorePattern: '^_', caughtErrorsIgnorePattern: '^_+$|^_?(e|err|error)$' }],
      'no-console': 'off',     // existing codebase logs liberally; not a fight worth picking
      'no-empty': ['warn', { allowEmptyCatch: true }],
      'no-prototype-builtins': 'warn',
      'no-useless-escape': 'warn',
      'no-case-declarations': 'warn',

      // ── P120 / V-1 V-2 V-5 — Visual budget guardrails ────────────────
      // Three local rules surface design-system drift. They started as
      // warnings while the codebase carried legacy violations; the P120/
      // P121 burn-down migrated every raw fontSize, color, and inline
      // button verb to a token (zero rendered change). Count is now zero,
      // so they are promoted to ERROR — new drift fails the gate.
      'visual-budget/no-raw-fontsize':   'error',
      'visual-budget/no-raw-color':      'error',
      'visual-budget/no-raw-button-copy': 'error',

      // ── P146 — Funnel/analytics event-name contract ───────────────────
      // Event names passed to track()/Funnel.track() must be EVENTS.*
      // constants, never raw strings or template literals. The runtime
      // whitelist only warns in DEV and drops in prod; this catches the
      // typo statically. ERROR (not warn) because every existing call
      // site already complies — there is no legacy debt to migrate, so
      // the contract can be enforced hard from day one.
      'analytics/funnel-event-contract': 'error',
      // Props must be coarse — never names/prose/secrets or whole domain objects.
      'analytics/analytics-props-hygiene': 'error',
    },
  },

  // ── Determinism guard — no raw non-determinism in the seeded pipeline ──────
  // Generators must draw randomness through rngContext (random/pick/chance/…)
  // so a settlement replays byte-exact from its stored seed. A bare
  // Math.random(), Date.now(), or new Date() in generator logic is a silent
  // determinism leak the gate could not see before (rngContext's fallback
  // converts it into invisible non-reproducibility). Errors here, so a new
  // leak fails CI. prng.js (the documented sole non-determinism entry, for
  // seed minting) and rngContext.js (the fallback itself) are exempt.
  {
    files: ['src/generators/**/*.js'],
    ignores: ['src/generators/prng.js', 'src/generators/rngContext.js'],
    rules: {
      'no-restricted-syntax': ['error',
        {
          selector: "CallExpression[callee.object.name='Math'][callee.property.name='random']",
          message: 'Determinism: use random()/pick()/chance()/randInt() from rngContext.js, not Math.random() — a raw draw breaks same-seed replay.',
        },
        {
          selector: "CallExpression[callee.object.name='Date'][callee.property.name='now']",
          message: 'Determinism: Date.now() is non-reproducible in the seeded pipeline. Thread a timestamp in instead.',
        },
        {
          selector: "NewExpression[callee.name='Date'][arguments.length=0]",
          message: 'Determinism: new Date() reads wall-clock time and breaks same-seed replay. Construct from an explicit value.',
        },
      ],
    },
  },

  // ── Accessibility (jsx-a11y) — ERROR (hardened 2026-06) ──────────────────────
  // The component/PDF JSX layer is excluded from tsc and had no a11y linting, so
  // accessibility gaps accumulated invisibly. These started at WARN for an
  // incremental cleanup; once the recommended-rule count reached zero (WS3
  // burn-down across 55 files) they were promoted to ERROR so regressions block
  // the gate — mirroring how the visual-budget rules were hardened. A handful of
  // genuinely-intentional cases (focus-on-open inline editors, the deprecated
  // label-has-for vs label-has-associated-control split) carry justified
  // per-line eslint-disable comments.
  {
    files: ['src/**/*.jsx'],
    plugins: { 'jsx-a11y': jsxA11y },
    rules: Object.fromEntries(
      Object.keys(jsxA11y.flatConfigs.recommended.rules).map(rule => [rule, 'error']),
    ),
  },

  // Data tables: arrow functions are written with uniform signatures
  // (e.g. `(r, s) => ...`) even when an individual entry only uses one
  // parameter. The shape is intentional — it lets callers invoke any
  // entry without branching. Flagging "unused" args here would just
  // produce noise.
  {
    files: ['src/data/**/*.{js,jsx}'],
    rules: {
      'no-unused-vars': 'off',
    },
  },

  // Test files: looser rules. Tests use describe/test/expect globals
  // (vitest) and frequently shadow names within nested describes.
  {
    files: ['tests/**/*.{js,jsx}'],
    languageOptions: {
      globals: { ...globals.node, ...globals.es2024 },
    },
    rules: {
      'no-unused-vars': 'off',
    },
  },

  // Node-side scripts: enable node globals, allow process/require.
  {
    files: ['scripts/**/*.{js,mjs,cjs}'],
    languageOptions: {
      globals: { ...globals.node, ...globals.es2024 },
      sourceType: 'module',
    },
    rules: {
      'no-unused-vars': ['warn', { argsIgnorePattern: '^_' }],
    },
  },
];
